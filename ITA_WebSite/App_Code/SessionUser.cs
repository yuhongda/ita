using System;
using System.Collections.Generic;
using System.Text;


public class SessionUser
{
    private string _UserID;
    public string UserID
    {
        get
        {
            return _UserID;
        }
        set
        {
            _UserID = value;
        }
    }

    private string _UserName;
    public string UserName
    {
        get
        {
            return _UserName;
        }
        set
        {
            _UserName = value;
        }
    }

    private List<string> _Roles;
    public List<string> Roles
    {
        get
        {
            return _Roles;
        }
        set
        {
            _Roles = value;
        }
    }

    private string _Email;
    public string Email
    {
        get
        {
            return _Email;
        }
        set
        {
            _Email = value;
        }
    }

    public SessionUser(string userID, string userName, List<string> roles, string email)
    {
        this.UserID = userID;
        this.UserName = userName;
        this.Roles = roles;
        this.Email = email;
    }
}
