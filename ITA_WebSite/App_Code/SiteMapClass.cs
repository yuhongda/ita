using System;
using System.Data;
using System.Configuration;
using System.Web;
using System.Web.Security;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Web.UI.WebControls.WebParts;
using System.Web.UI.HtmlControls;

/// <summary>
/// SiteMapClass 的摘要说明
/// </summary>
public class SiteMapClass
{
    private string _id;
    public string ID
    {
        get
        {
            return this._id;
        }
        set
        {
            this._id = value;
        }
    }

    private string _title;
    public string Title
    {
        get
        {
            return this._title;
        }
        set
        {
            this._title = value;
        }
    }

    private string _description;
    public string Description
    {
        get
        {
            return this._description;
        }
        set
        {
            this._description = value;
        }
    }

    private string _url;
    public string Url
    {
        get
        {
            return this._url;
        }
        set
        {
            this._url = value;
        }
    }

    private string _enable;
    public string Enable
    {
        get
        {
            return this._enable;
        }
        set
        {
            this._enable = value;
        }
    }



    public SiteMapClass(string id, string title, string description, string url, string enable)
    {
        this.ID = id;
        this.Title = title;
        this.Description = description;
        this.Url = url;
        this.Enable = enable;
    }
}
